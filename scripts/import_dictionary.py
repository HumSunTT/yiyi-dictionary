def import_ecdict(csv_path: str, db_path: str, limit: int = None):
    """从 ECDICT CSV 导入英汉词典"""
    
    print(f"正在导入词典数据...")
    print(f"CSV文件: {csv_path}")
    print(f"数据库: {db_path}")
    
    # 连接数据库
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    
    cursor.execute('DROP TABLE IF EXISTS english_dict')
    
    cursor.execute('''
        CREATE TABLE english_dict (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            word TEXT NOT NULL,
            phonetic TEXT,
            pos TEXT,
            definition TEXT NOT NULL,
            examples TEXT,
            frequency INTEGER DEFAULT 0
        )
    ''')
    
    # 创建索引
    cursor.execute('CREATE INDEX IF NOT EXISTS idx_english_word ON english_dict(word)')
    
    # 清空现有数据
    cursor.execute('DELETE FROM english_dict')
    
    # 读取CSV并导入
    count = 0
    batch = []
    batch_size = 1000
    
    with open(csv_path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        
        for row in reader:
            if limit and count >= limit:
                break
            
            word = row.get('word', '').strip()
            if not word:
                continue
            
            phonetic = row.get('phonetic', '').strip()
            translation = row.get('translation', '').strip()
            pos = row.get('pos', '').strip()
            collins = int(row.get('collins', 0) or 0)
            oxford = int(row.get('oxford', 0) or 0)
            tag = row.get('tag', '').strip()
            frq = int(row.get('frq', 0) or 0)
            
            # 跳过没有翻译的词
            if not translation:
                continue
            
            batch.append((
                word,
                phonetic,
                pos,
                translation,
                None,
                frq
            ))
            
            count += 1
            
            if len(batch) >= batch_size:
                cursor.executemany('''
                    INSERT INTO english_dict 
                    (word, phonetic, pos, definition, examples, frequency)
                    VALUES (?, ?, ?, ?, ?, ?)
                ''', batch)
                conn.commit()
                batch = []
                print(f"\r已导入: {count} 词", end='', flush=True)
    
    if batch:
        cursor.executemany('''
            INSERT INTO english_dict 
            (word, phonetic, pos, definition, examples, frequency)
            VALUES (?, ?, ?, ?, ?, ?)
        ''', batch)
        conn.commit()
    
    print(f"\n✅ 导入完成: {count} 词")
    
    # 统计
    total = cursor.execute('SELECT COUNT(*) FROM english_dict').fetchone()[0]
    print(f"数据库总词数: {total}")
    
    conn.close()
    return count


